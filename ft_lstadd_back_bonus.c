#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*tmp;

	tmp = *lst;
	if (!(*lst))
		*lst = new;
	else
	{
		ft_lstlast(tmp);
		tmp->next = new;
	}
}
